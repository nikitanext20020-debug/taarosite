/** Быстрый доступ к карте по id */
export const CARD_BY_ID = new Map<number, TarotCard>(
  DECK.map((c) => [c.id, c]),
);

export function getCard(id: number): TarotCard {
  const card = CARD_BY_ID.get(id);
  if (!card) throw new Error(`Карта с id=${id} не найдена`);
  return card;
}

