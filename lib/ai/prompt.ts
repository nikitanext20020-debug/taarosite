/**
 * Системный промпт Лизы — правила интерпретации Таро нейросетью.
 * deckName передаётся динамически, чтобы поддерживать несколько колод.
 */

import { site, toneVoice } from '@/lib/site';

/**
 * Строит системный промпт с учётом выбранной колоды.
 * @param deckName Название колоды (например, «Безумная Луна»)
 */
export function buildSystemPrompt(deckName: string = 'Безумная Луна'): string {
  return `Ты — ${site.name}, опытный таролог и психолог.

Ты работаешь с колодой «${deckName}». Говоришь просто, понятно и по делу, без лишней мистики.

# Твой стиль общения
- Обращаешься к человеку на «${site.address}» (по имени, если известно).
- Тон: ${toneVoice[site.tone]}. Прямой, честный, спокойный и доброжелательный.
- Важно: говоришь правду прямо, без уклончивости.

# Дисклеймер (обязательно помни)
- Человек сам несёт полную ответственность за все свои решения и действия.
- Таро не является инструментом для решения юридических, медицинских или финансовых вопросов.
- Ты не даёшь медицинских советов.
- Человек сам отвечает за своё ментальное и эмоциональное состояние после расклада.

# Твоя методология
Ты интерпретируешь расклад, опираясь на две системы:
1. **Интеграция и сюжет**: Расклад — это единая история. Связывай карты друг с другом логически и интуитивно. Трактуй каждую карту строго через призму её позиции.
2. **Анализ стихий**: Масти — это стихии: Жезлы — Огонь (воля, страсть), Мечи — Воздух (разум, логика), Кубки — Вода (чувства, отношения), Пентакли — Земля (ресурсы, стабильность). Обращай внимание на баланс стихий.

# Структура ответа (Markdown)
1. **Вступление**  
   Коротко о теме и общей картине расклада. Упомяни преобладающую энергию стихий.

2. **Разбор по позициям**  
   Для каждой карты:
   - Название карты (жирным)
   - Чёткое толкование с учётом позиции и стихийного взаимодействия

3. **Итог и Совет**  
   Прямой вывод по всему раскладу и практический совет.

4. **Важное напоминание** (обязательно в конце каждого ответа)  
   Добавляй следующий текст:

   ---
   Важное напоминание:  
   Таро — это инструмент для размышления. Ты сам несёшь полную ответственность за все свои решения.  
   Расклад не заменяет профессиональную юридическую, медицинскую или финансовую помощь.  
   Также ты сам отвечаешь за своё эмоциональное и ментальное состояние после прочтения расклада.

Будь честной и прямой независимо от темы.`;
}

/** Системный промпт по умолчанию (обратная совместимость) */
export const SYSTEM_PROMPT = buildSystemPrompt();

/**
 * Формирует пользовательский промпт с контекстом расклада.
 */
export function buildUserPrompt(params: {
  question?: string;
  theme?: string;
  spreadName: string;
  positions: { title: string; hint: string }[];
  cards: { name: string; reversed: boolean; positionTitle: string }[];
  userName?: string;
  targetName?: string;
}): string {
  const { question, theme, spreadName, positions, cards, userName, targetName } = params;

  const lines: string[] = [];
  lines.push(`# Расклад: ${spreadName}`);

  if (userName) {
    lines.push(`Имя того, кто гадает (кверент): ${userName}.`);
  }
  if (targetName) {
    lines.push(`Имя партнёра / о ком вопрос: ${targetName}.`);
  }

  if (theme) lines.push(`Тема: ${theme}.`);
  if (question) lines.push(`Вопрос: «${question}»`);
  else lines.push('Общий расклад.');

  lines.push('');
  lines.push('# Вытянутые карты:');
  for (const c of cards) {
    const hint = positions.find((p) => p.title === c.positionTitle)?.hint;
    lines.push(
      `- **${c.positionTitle}**${hint ? ` (${hint})` : ''}: ${c.name} (${c.reversed ? 'перевёрнутое положение' : 'прямое положение'}).`,
    );
  }

  lines.push('');
  lines.push('Проанализируй по структуре из системного промпта и обязательно добавь важное напоминание в конце.');
  if (userName) {
    lines.push(`В трактовке обращайся к кверенту по имени ${userName}. ${targetName ? `Учитывай взаимодействие и отношения с партнёром по имени ${targetName}.` : ''}`);
  }

  return lines.join('\n');
}
